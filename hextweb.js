/*
 Intel HEX format example
 :040bf000ffffffcf35
    +--------------------- Byte count
    |    +---------------- Address
    |    |  +------------- Record type (00:Data, 01:EOF, 04: Extended linear addres
    |    |  |        +---- Data
    |    |  |        |  +- Checksum
    |    |  |        |  |
 : 04 0bf0 00 ffffffcf 35
*/
hexfile.addr32=0;
hexfile.line=function(text){
	var i,darray;
	// Confirm checksum
	var csum=0;
	for(i=0;i<text.length-2;i+=2){
		csum+=parseInt(text.substr(i,2),16);
	}
	csum=(((csum&0xff)^0xff)+1)&0xff;
	if (csum!=parseInt(text.substr(text.length-2),16)) return 1;
	text=text.substr(0,text.length-2);
	// Fetch data
	var bytes=parseInt(text.substr(0,2),16);
	var addr=parseInt(text.substr(2,4),16);
	var type=parseInt(text.substr(6,2),16);
	var data=text.substr(8);
	switch(type){
		case 0:  // data
			if (bytes&3) {
				// Data must be array of 32 bit data
				return 1;
			}
			bytes>>=2;
			darray=new Array();
			for(i=0;i<bytes;i++){
				darray[i]=
					parseInt(data.substr(0,2),16)|
					parseInt(data.substr(2,2),16)<<8|
					parseInt(data.substr(4,2),16)<<16|
					parseInt(data.substr(6,2),16)<<24;
				data=data.substr(8);
			}
			system.loadFlash(this.addr32|addr,darray);
			return 0;
		case 4:  // extended linear address
			if (bytes!=2) {
				return 1;
			}
			this.addr32=parseInt(data,16)<<16;
			return 0;
		case 1:  // EOF
		default: // error
			return 1;
	}
};
hexfile.load=function(){
	var data=this.data;
	var m;
	while(m=data.match(/^[\r\n]*:(.*?)[\r\n]+/)){
		data=data.substr(m[0].length);
		if (this.line(m[1])) break;
	}
	if (data.match(/^[\r\n]*:(.*?)[\r\n]+/)) {
		// Error occured.
		dom.log('Hexfile error:'+m[0]);
	}
};
