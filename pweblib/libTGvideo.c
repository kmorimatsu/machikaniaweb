void gputfont(int x,int y,unsigned int c,int bc,unsigned char n)
//8*8ドットのアルファベットフォント表示
//座標（x,y)、カラー番号c
//bc:バックグランドカラー、負数の場合無視
//n:文字番号
{
	int i,j;
	unsigned char d;
	const unsigned char *p;

	p=GFontData+n*8;
	for(i=0;i<8;i++){
		d=*p++;
		for(j=0;j<8;j++){
			if(d&0x80) pset(x+j,y+i,c);
			else if(bc>=0) pset(x+j,y+i,bc);
			d<<=1;
		}
	}
}

